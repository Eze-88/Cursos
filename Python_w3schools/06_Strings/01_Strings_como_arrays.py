#Un string es un array de caracteres, asique se puede acceder a cada elemento en particular
x="Murcielago"
print(x)
#el array empieza en 0, asique se imprime el caracter del 4to lugar
print(x[3])
#se puede acceder a un rango, se llama slicing
print(x[4:7])
#se puede comenzar desde el final del string
print(x[-4:-1])
print(x[-1])